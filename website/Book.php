        <h1>The perfect girl.</h1>
		<h4 style="padding-left:30%">--In the aftermath of Murder--</h4>
		<img src="style/tfg.jpg" class="center"><p class="center">Poster</p>
        <p>In as little as 24 hours, your life can irrevocably change. Gilly Macmillan unflinchingly explores this reality in her second novel, The Perfect Girl. A year after publishing her bestselling, Edgar-nominated debut novel, What She Knew, Macmillan captivates readers with a story just as addictive as her first.</p>
        <p>The titular 'Perfect' girl is 17-year-old genius musician Zoe. Zoe and her mother, Maria, have been outcast from their former community after Zoe was found guilty of driving under the influence and killing three local teenagers, including her best friend. Maria has remarried into what is dubbed the 'Second Chance Family', which includes new husband Chris and his teenage son, Lucas, both oblivious to Zoe and Maria's marred past. Their lives are intertwined with Sam, Zoe's former lawyer; Tessa, Zoe's aunt and Maria's sister; and Richard, Tessa's loving yet alcoholic husband.</p>
        <p>The relationships in the 'Second Chance Family' are fraught with secrets. Brooding Lucas is obviously carrying around the weight of his own past, while Maria maintains a precarious presentation of herself and Zoe to meet the expecations of suspiciously controlling Chris. When Maria is found dead, the thread holding everyone together unravels during the next 24 hours. Having already been institutionalized, Zoe fears she will become the prime suspect, but those closest to Maria and Zoe cannot be discounted as potential murderers.</p>
		<p>Macmillan shines when exploring the intricacies of relationships, and the ties that bind this family are strained and complicated indeed. The story is told from the perspective of only three narrators: Zoe, Tessa and Sam. Yet through these lenses, we gain intimate insight into the other characters in whom we have become so invested. Macmillan adeptly demonstrates through her chosen method of storytelling that 24 hours can pass in what seems like a second—or a lifetime.</p>
        <p>Macmillan has provided a follow-up novel possibly even better than her first, and fans of Tana French, Ruth Ware and Gillian Flynn will become completely entrenched in the unfolding details.</p>
		<h3 align="right">-Vani Vinod</h3>
