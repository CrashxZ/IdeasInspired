        <h1>Deadpool.</h1>
		<img src="style/dp.jpg" class="center" style="width:320px;height:463px"><p class="center">Poster</p>
        <p>After a bunch of shitty movies like ‘Green Lantern’ and his appearance in ‘X-Men Origins’ , Rayan Reynolds has finally redeemed himself in this Science fiction superhero flick, DeadPool!</p>
		<p>This movie is a smirking deconstruction of all the superhero movie excesses and clichés seen by far as it reinvents the “ManIn-The-Tights” genre with action sequences so violent and unflinching, and the humour so crude and twisted, it will challenge the most seasoned cinephiles to find a film that pulls off both as well. But, if there are any real (super) heroes in this production, it's the writers. The script, loaded with selfreferences, its takes on the superhero movie clichés, its bashing of production studios and of course, the sex jokes, crafted in such a manner that you'll be hard-pressed to find a single dull moment.</p>
		<p>From the very opening credits, it is clear that the creators of this one are not planning to hold back. In fact, keeping up with the notion of bringing something new to the table, every trick in the book is used to make it as outlandish and shocking as possibly, all the while dodging the pitfalls of such efforts.</p>
		<p>A former Special Forces operative turned mercenary, Wade Wilson played by Rayan Reynolds, is subjected to a rogue experiment that leaves him with accelerated healing powers and adopts the alter ego Deadpool. Ed Skrein plays the bad-guy Ajax, who is being hunted by Deadpool so that he can exact some revenge after Ajax (as Deadpool puts it) "fu**ed up his face" in the experiment, which gave 'Pool his powers to begin with. But, as the film insists, this is also a love story and Deadpool's love is Vanessa, played by Morena Baccarin.</p>
		<p>Although the major theme of the movie revolves around Wade's (Deadpool) attempts to reunite with Vanessa and save her from “not-so-good guys”, the "love story" completely blends into the overall hilarious tone of the movie. All the actors in the flick were great but Rayan Reynolds breaks the fourth wall by his performance in the movie. The fact that he realizes him being present in the movie is mind blowing. Deadpool was released on Valentine’s Day (14th February), if you’ve been waiting for the perfect Valentine’s Day movie, your wait is over –This is it!  </p>
